CREATE OR REPLACE FUNCTION get_balance(p_account_id NUMBER)
RETURN NUMBER
IS
    v_balance NUMBER;
BEGIN
    SELECT balance INTO v_balance
    FROM accounts
    WHERE account_id = p_account_id;

    RETURN v_balance;

EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RETURN 0;
END;
/

CREATE OR REPLACE PROCEDURE process_transaction (
    p_account_id NUMBER,
    p_type VARCHAR2,
    p_amount NUMBER
)
IS
    v_balance NUMBER;

    insufficient_funds EXCEPTION;
BEGIN
    -- Get current balance
    v_balance := get_balance(p_account_id);

    -- Check withdrawal
    IF p_type = 'WITHDRAW' AND v_balance < p_amount THEN
        RAISE insufficient_funds;
    END IF;

    -- Insert transaction
    INSERT INTO transactions (
        account_id,
        transaction_type,
        amount,
        status
    )
    VALUES (
        p_account_id,
        p_type,
        p_amount,
        'SUCCESS'
    );

EXCEPTION
    WHEN insufficient_funds THEN
        INSERT INTO transactions (
            account_id,
            transaction_type,
            amount,
            status
        )
        VALUES (
            p_account_id,
            p_type,
            p_amount,
            'FAILED'
        );

        DBMS_OUTPUT.PUT_LINE('Insufficient funds');

    WHEN OTHERS THEN
        DBMS_OUTPUT.PUT_LINE('Error occurred');
END;
/

CREATE OR REPLACE TRIGGER trg_update_balance
AFTER INSERT ON transactions
FOR EACH ROW
BEGIN
    IF :NEW.transaction_type = 'DEPOSIT' AND :NEW.status = 'SUCCESS' THEN
        UPDATE accounts
        SET balance = balance + :NEW.amount
        WHERE account_id = :NEW.account_id;

    ELSIF :NEW.transaction_type = 'WITHDRAW' AND :NEW.status = 'SUCCESS' THEN
        UPDATE accounts
        SET balance = balance - :NEW.amount
        WHERE account_id = :NEW.account_id;
    END IF;
END;
/

CREATE OR REPLACE TRIGGER trg_audit_log
AFTER INSERT ON transactions
FOR EACH ROW
BEGIN
    INSERT INTO audit_logs (
        action_type,
        description
    )
    VALUES (
        'TRANSACTION',
        'Transaction ID: ' || :NEW.transaction_id ||
        ', Type: ' || :NEW.transaction_type ||
        ', Amount: ' || :NEW.amount
    );
END;
/

