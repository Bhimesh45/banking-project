CREATE TABLE customers (
    customer_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR2(100),
    email VARCHAR2(100),
    phone VARCHAR2(15),
    created_at DATE DEFAULT SYSDATE
);

CREATE TABLE accounts (
    account_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_id NUMBER,
    account_type VARCHAR2(20),
    balance NUMBER(12,2) DEFAULT 0,
    created_at DATE DEFAULT SYSDATE,
    
    CONSTRAINT fk_customer
    FOREIGN KEY (customer_id)
    REFERENCES customers(customer_id)
);

CREATE TABLE transactions (
    transaction_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    account_id NUMBER,
    transaction_type VARCHAR2(20),
    amount NUMBER(12,2),
    transaction_date DATE DEFAULT SYSDATE,
    status VARCHAR2(20),
    
    CONSTRAINT fk_account
    FOREIGN KEY (account_id)
    REFERENCES accounts(account_id)
);

CREATE TABLE kyc_documents (
    kyc_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_id NUMBER,
    document_type VARCHAR2(50),

    -- 🔥 All 3 types
    document_blob BLOB,         -- store file inside DB
    document_clob CLOB,         -- store large text (description/log)
    document_bfile BFILE,       -- external file reference

    uploaded_at DATE DEFAULT SYSDATE,

    CONSTRAINT fk_kyc_customer
    FOREIGN KEY (customer_id)
    REFERENCES customers(customer_id)
);

CREATE TABLE audit_logs (
    log_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    action_type VARCHAR2(50),
    description CLOB,
    created_at DATE DEFAULT SYSDATE
);

